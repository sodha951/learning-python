name = " ALPESH IS A BOY "              

print(len(name))   # Length of the string                                                                            
print(name.endswith("esh"))  # Check if the string ends with "esh"                  
print(name.startswith("Alp")) # Check if the string starts with "Alp"              
print(name.capitalize())     # Capitalize the first letter of the string       
print(name.lower())      # Convert the string to lowercase     
print(name.upper())    # Convert the string to uppercase    
print(name.title())  # capitalize first letter of each word in a sentence       
print(name.find("IS"))  # Returns the lowest index where the substring sub is found. Returns -1 if not found.     
print(name.find("E"))    # Returns the lowest index where the substring sub is found. Returns -1 if not found.    
print(name.split())     # Splits the string into a list of substrings based on the specified separator.               
print(name.strip())  

