i = 1

while(i<6): #The block keeps executing until the condition is true
    print(i)
    i += 1 # i = i + 1
    
# In while loops,the condition is checked first.it is evaluates to true,the body of the loop is executed otherwise not!

# If the loop is entered,the process of [condition check & execution] is continued until the condition becomes false.

